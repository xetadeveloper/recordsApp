    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        //Server
        config.setHost(txtHost.getText().trim());
        config.setPort(Integer.parseInt(txtPort.getText().trim()));

        //Log Files
        config.setErrorLog(txtError.getText().trim());
        config.setMessageLog(txtMessage.getText().trim());

        //Store data
        if (config.storeData()) {
            JOptionPane.showMessageDialog(this, "Settings Saved!", "Success", JOptionPane.INFORMATION_MESSAGE);
            int option = JOptionPane.showConfirmDialog(this, "Application needs to be restarted for changes to take place", "Restart Application", JOptionPane.YES_NO_OPTION);

            btnEdit.setEnabled(true);

            if (option == JOptionPane.YES_OPTION) {
                client.sendQueryUTF("exit");
                System.exit(0);
            } else {
                dispose();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Unable to save, Check backing store or restart application", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        //enable all fields and save button then disable itself
        if (JOptionPane.showConfirmDialog(this, "Do you want to edit these settings?", "Edit", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            enableAllFields();
            btnSave.setEnabled(true);
            btnEdit.setEnabled(false);
        }
    }//GEN-LAST:event_btnEditActionPerformed

