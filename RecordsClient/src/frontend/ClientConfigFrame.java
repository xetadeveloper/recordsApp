    /**
     * Enables all text fields and password fields for editing
     */
    private void enableAllFields() {
        txtError.setEditable(true);
        txtHost.setEditable(true);
        txtMessage.setEditable(true);
        txtPort.setEditable(true);
        btnBrowseError.setEnabled(true);
        btnBrowseLogNormal.setEnabled(true);
    }

    private void initFields() {
        //Log
        txtError.setText(config.getErrorLog());
        txtMessage.setText(config.getMessageLog());

        //Server
        txtPort.setText(String.valueOf(config.getPort()));
        txtHost.setText(config.getHost());
    }

    /**
     * This sets the url selected in the chooser to the textfield
     *
     * @param field
     * @param chooser
     */
    private void browseAndSetUrl(JTextField field, JFileChooser chooser, String fileName) {
        initFileChooser(chooser);
        int open = chooser.showOpenDialog(this);

        if (open == JFileChooser.APPROVE_OPTION) {
            String directory = chooser.getSelectedFile().getAbsolutePath();

            Path path = Paths.get(directory + "\\" + folderName + "\\" + fileName);
            field.setText(path.toAbsolutePath().toString().trim());
        }
    }

    /**
     * Initializes the JFileChooser with style and dimensions if necessary
     *
     * @param chooser
     */
    private void initFileChooser(JFileChooser chooser) {
        chooser.setCurrentDirectory(Paths.get(System.getenv("ProgramFiles")).getParent().toAbsolutePath().toFile());
    }

