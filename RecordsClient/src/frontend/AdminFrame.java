    /**
     * When the college combo changes value initialize the dept combo with
     * different values
     */
    private void onChangeSelectedCollege() {

        String selected = cmbCollege.getSelectedItem().toString();

        int colID = collegeFull.stream().filter(college -> college.toString().equalsIgnoreCase(selected.trim())).findFirst().orElse(null).getCollegeId();

        //Depts
        DefaultComboBoxModel model = (DefaultComboBoxModel) cmbDept.getModel();
        model.removeAllElements();

        for (Department dept : deptFull) {
            if (dept.getCollegeId() == colID) {
                model.addElement(dept.toString());
            }
        }

        cmbDept.setModel(model);
        cmbDept.setSelectedIndex(0);
    }

    /**
     * When the dept combo changes value initialize the programme combo with
     * different values
     */
    private void onChangeSelectedDept() {
        if (cmbDept.getSelectedItem() != null) {
            String selected = cmbDept.getSelectedItem().toString();

            int deptID = deptFull.stream().filter(dept -> dept.toString().equalsIgnoreCase(selected.trim())).findFirst().orElse(null).getDeptId();

            //Depts
            DefaultComboBoxModel model = (DefaultComboBoxModel) cmbProgramme.getModel();
            model.removeAllElements();

            for (Programme prog : progFull) {
                if (prog.getDeptId() == deptID) {
                    model.addElement(prog.toString());
                }
            }

            cmbProgramme.setModel(model);
            cmbProgramme.setSelectedIndex(0);
        }
    }

    /**
     * Adds a document listener to the text field for search purposes
     *
     * @param field
     */
    private void initDocumentListener(JTextField field) {
        field.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                String text = field.getText().trim();
                searchText += text.charAt(text.length() - 1);
                searchStudent(tblStudents, studentList, searchType, searchText, columnNames);
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                if (searchText.length() > 0) {
                    searchText = searchText.substring(0, searchText.length() - 1);
                    searchStudent(tblStudents, studentList, searchType, searchText, columnNames);
                } else {
                    initTableRecords(tblStudents, studentList, columnNames);
                }
            }

            @Override
            public void changedUpdate(DocumentEvent e) {

            }
        });
    }

