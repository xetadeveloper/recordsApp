    /**
     * Initializing the various panels with data gotten from server
     */
    private void initStudentDetailsPanel(Student student, College college, Programme prog, Department dept, boolean edit) {
        String approved = "Approved", unapproved = "Not Approved", notStarted = "Not Started", started = "Started", finished = "Finished", pending = "Pending",
                awaiting = "Awaiting Approval", yes = "Yes", no = "No";

        if (edit) {
            Programme stuProg = progFull.stream().filter(progL -> progL.getProgrammeId() == student.getProgrammeId()).findFirst().get();
            Department stuDept = deptFull.stream().filter(dep -> dep.getDeptId() == stuProg.getDeptId()).findFirst().get();
            College stuCol = collegeFull.stream().filter(col -> col.getCollegeId() == stuDept.getCollegeId()).findFirst().get();

            lblMatricNoEdit.setText(checkNull(student.getMatricNo()));
            lblCAApprovedEdit.setText(checkNull(student.isCourseAdvisorApproved() ? "Approved" : "Not Approved"));
            lblHODApprovedEdit.setText(checkNull(student.isHODApproved() ? "Approved" : "Not Approved"));
            lblDeanApprovedEdit.setText(checkNull(student.isDeanApproved() ? "Approved" : "Not Approved"));
            txtFirstName.setText(checkNull(student.getFirstName()));
            txtLastName.setText(checkNull(student.getLastName()));
            lblLevelEdit.setText(checkNull(student.getLevel()));
            lblRegStatus.setText(student.isRegistrationAllowed() ? (student.isCourseAdvisorApproved() ? (student.isDeanApproved() ? finished : started) : pending) : notStarted);
            lblStuReg.setText(student.isStuRegistered() ? ((!student.isCourseAdvisorApproved()) || (!student.isHODApproved()) || (!student.isDeanApproved()) ? awaiting : yes) : no);
            lblAdmin.setText(student.isRegistrationAllowed() ? approved : unapproved);
            setLabelImage(student.getPassport(), lblPassportEdit);

            //init combos
            initCombo(cmbCollege, collegeFull);

            //set combo default values
            int count = 0;
            for (College col : collegeFull) {
                if (col.getCollegeId() == stuCol.getCollegeId()) {
                    cmbCollege.setSelectedIndex(count);
                } else {
                    ++count;
                }
            }

            count = 0;
            for (Department dep : deptFull) {
                if (dep.getDeptName().equalsIgnoreCase(stuDept.getDeptName())) {
                    cmbDept.setSelectedItem(dep.getDeptName());
                } else {
                    ++count;
                }
            }

            for (Programme dep : progFull) {
                if (dep.getProgrammeName().equalsIgnoreCase(stuProg.getProgrammeName())) {
                    cmbProgramme.setSelectedItem(dep.getProgrammeName());
                } else {
                    ++count;
                }
            }
        } else {
            lblMatricNo.setText(checkNull(student.getMatricNo()));
            lblFirstName.setText(checkNull(student.getFirstName()));
            lblLastName.setText(checkNull(student.getLastName()));
            lblCollege.setText(checkNull(college.getCollegeName()));
            lblDept.setText(checkNull(dept.getDeptName()));
            lblProgramme.setText(checkNull(prog.getProgrammeName()));
            lblLevel.setText(checkNull(student.getLevel()));
            lblCAApproved.setText(checkNull(student.isCourseAdvisorApproved() ? "Approved" : "Not Approved"));
            lblHODApproved.setText(checkNull(student.isHODApproved() ? "Approved" : "Not Approved"));
            lblDeanApproved.setText(checkNull(student.isDeanApproved() ? "Approved" : "Not Approved"));
            lblRegStatus.setText(student.isRegistrationAllowed() ? (student.isCourseAdvisorApproved() ? (student.isDeanApproved() ? finished : started) : pending) : notStarted);
            lblStuReg.setText(student.isStuRegistered() ? ((!student.isCourseAdvisorApproved()) || (!student.isHODApproved()) || (!student.isDeanApproved()) ? awaiting : yes) : no);
            lblAdmin.setText(student.isRegistrationAllowed() ? approved : unapproved);
            setLabelImage(student.getPassport(), lblPassport);
        }
    }

    private void initFinancialReportPanel(Student student, Programme prog, boolean edit) {
        NumberFormat form = NumberFormat.getInstance(Locale.US);

        if (edit) {
            lblOriginalFeesEdit.setText(checkNull(form.format(prog.getProgrammeFee())));
            lblPaidFeesEdit.setText(checkNull(form.format(prog.getProgrammeFee() - student.getFeeBalance())));
            lblBalanceFeesEdit.setText(checkNull(form.format(student.getFeeBalance())));
        } else {
            lblOriginalFees.setText(checkNull(form.format(prog.getProgrammeFee())));
            lblPaidFees.setText(checkNull(form.format(prog.getProgrammeFee() - student.getFeeBalance())));
            lblBalanceFees.setText(checkNull(form.format(student.getFeeBalance())));
        }
    }

    private void initAcademicReportPanel(Student student, SessionDetails session, CarryOvers carry, RegisteredCourses reg, boolean edit) {
        if (edit) {
            lblGpaEdit.setText(checkNull(student.getGpa()));
            lblMaxUnitsEdit.setText(checkNull(session.getMaximumUnits()));

            //This will be a calculation of total units registered
            lblRegisteredUnitsEdit.setText(String.valueOf(getTotalRegisteredUnits(reg)));

            ///for the reg table and carry over table
            if (carry != null) {
                ArrayList<Course> courses = carry.getCarriedCourses();
                if (courses.size() > 0) {
                    initTable(tblCarriedEdit, courses);
                } else {
                    initTableNoData(tblCarriedEdit);
                }
            } else {
                initTableNoData(tblCarriedEdit);
            }

            if (reg != null) {
                ArrayList<Course> regs = reg.getRegisteredCourses();
                if (regs.size() > 0) {
                    initTable(tblRegisteredEdit, regs);
                } else {
                    initTableNoData(tblRegisteredEdit);
                }
            } else {
                initTableNoData(tblRegisteredEdit);
            }
        } else {
            lblGpa.setText(checkNull(student.getGpa()));
            lblMaxUnits.setText(checkNull(session.getMaximumUnits()));

            //This will be a calculation of total units registered
            lblRegisteredUnits.setText(String.valueOf(getTotalRegisteredUnits(reg)));

            //for the reg table and carry over table
            if (carry != null) {
                ArrayList<Course> courses = carry.getCarriedCourses();
                if (courses.size() > 0) {
                    initTable(tblCarried, courses);
                } else {
                    initTableNoData(tblCarried);
                }
            } else {
                initTableNoData(tblCarried);
            }

            if (reg != null) {
                ArrayList<Course> regs = reg.getRegisteredCourses();
                if (regs.size() > 0) {
                    initTable(tblRegistered, regs);
                } else {
                    initTableNoData(tblRegistered);
                }
            } else {
                initTableNoData(tblRegistered);
            }
        }
    }

