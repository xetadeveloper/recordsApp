    /**
     * Creates and updates the database
     *
     * @return true/false
     */
    private boolean createAndSendUpdateQuery() {
        System.out.println("Inside create and send query method");
        boolean updated = false;
        String firstname = txtFirstName.getText().trim();
        String lastname = txtLastName.getText().trim();

        //for programme combos
        String prog = cmbProgramme.getSelectedItem().toString();
        int progID = 0;

        for (Programme progLocal : progFull) {
            if (progLocal.getProgrammeName().equalsIgnoreCase(prog.trim())) {
                System.out.println("Programme found in update");
                progID = progLocal.getProgrammeId();
                break;
            }
        }

        String query = String.format("update students set \n"
                + "students.FirstName = '%s',\n"
                + "students.LastName = '%s',\n"
                + "students.Student_ProgrammeID = %d,"
                + "where students.matricNo = %d;",
                firstname,
                lastname,
                progID,
                matricNo);

        client.sendQuery(UPDATE + ":" + query);

        updated = (Boolean) client.getResponse();

        return updated;
    }

