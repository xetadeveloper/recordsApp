    private void btnPayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPayActionPerformed
        if (validateTextNumber(txtPaying)) {
            double paying = getUnformattedCurrencyValue(txtPaying.getText().trim());
            double original = getUnformattedCurrencyValue(lblOriginalFees.getText().trim());
            double balance = original - paying;

            if (paying > original || paying < 0) {
                displayErrorMessage("The amount to be paid is higher than the original fees or negative", "Error updating payment details");
            } else {
                lblPaidFees.setText(getFormattedCurrencyValue(paying));
                lblBalanceFees.setText(getFormattedCurrencyValue(balance));
                txtPaying.setText("");
            }
        } else {
            displayMessage("A letter has been entered in the text field. Only numbers are allowed", "Error in input");
        }

    }//GEN-LAST:event_btnPayActionPerformed

