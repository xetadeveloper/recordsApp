    /**
     * Generates matric no for new student
     *
     * @param prog
     * @return
     */
    private String generateMatricNo(Programme prog) {
        int currentYear = LocalDate.now().getYear();
        String rep = String.valueOf(currentYear);
        String repMain = rep.substring(rep.length() - 2);

        int stuNumber = prog.getStudentYearNumber() + 1;
        int matricType = prog.getMatricType();
        String genMatricTemp = "" + matricType + (stuNumber <= 9 ? "0" + stuNumber : stuNumber);
        String genMatric = genMatricTemp.replace("15", repMain);
        return genMatric;
    }

    /**
     * Validates all necessary filled fields
     *
     * @return
     */
    @Override
    protected boolean validateAllFields() {
        boolean validData = true;

        if (!(fieldHasData(txtFirstName) && validateTextLetter(txtFirstName))) {
            setErrorBorder(txtFirstName, true);
            validData = false;
        } else {
            setErrorBorder(txtFirstName, false);
        }
        if (!(fieldHasData(txtLastName) && validateTextLetter(txtLastName))) {
            setErrorBorder(txtLastName, true);
            validData = false;
        } else {
            setErrorBorder(txtLastName, false);
        }
        if (!(fieldHasData(txtLevel) && validateTextNumber(txtLevel))) {
            setErrorBorder(txtLevel, true);
            validData = false;
        } else {
            setErrorBorder(txtLevel, false);
        }
        if (!(fieldHasData(txtGpa) && validateTextDecimal(txtGpa))) {
            setErrorBorder(txtGpa, true);
            validData = false;
        } else {
            setErrorBorder(txtGpa, false);
        }

        return validData;
    }

    /**
     * When the programme combo changes value initialize the payment label with
     * programme fee
     *
     * @param cmbDept
     * @param cmbProgramme
     */
    private void onChangeSelectedProg(JComboBox<String> cmbProg) {
        if (cmbProg.getSelectedItem() != null) {
            String selected = cmbProg.getSelectedItem().toString();
            Programme progMain = progFull.stream().filter(prog -> prog.toString().trim().equalsIgnoreCase(selected.trim())).findFirst().orElse(null);
            NumberFormat nf = NumberFormat.getInstance(Locale.US);
            lblOriginalFees.setText(String.valueOf(nf.format(progMain.getProgrammeFee())));
            lblPaidFees.setText("0");
            lblBalanceFees.setText("0");
            matricNo = generateMatricNo(progMain);
            lblMatricNoEdit.setText(matricNo);
            progGlobal = progMain;
        }
    }

