    @Override
    protected boolean validateAllFields() {
        boolean validData = true;

        if (!(fieldHasData(txtDeptName) && validateTextLetter(txtDeptName))) {
            setErrorBorder(txtDeptName, true);
            validData = false;
        } else {
            setErrorBorder(txtDeptName, false);
        }

        return validData;
    }

    protected void onChangeSelectedCollege(JComboBox<String> cmbCollege, JComboBox<String> cmbDeptHead, ArrayList<Staff> staffs) {
        //sets the department
        if (cmbCollege.getSelectedItem() != null) {
            String selected = cmbCollege.getSelectedItem().toString().trim();
            System.out.println("Selected Dept: " + selected);
            collegeGlobal = collegeFull.stream().filter(col -> col.toString().trim().equalsIgnoreCase(selected.trim())).findFirst().orElse(null);
            setDeptHeadCombo(cmbDeptHead, collegeGlobal);
        } else {
            System.out.println("Selected Item is null");
        }
    }

    protected void onChangeSelectedDeptHead(JComboBox<String> cmbDeptHead) {
        //sets the department
        if (cmbDeptHead.getSelectedItem() != null) {
            String selected = cmbDeptHead.getSelectedItem().toString().trim();
            System.out.println("Selected Staff: " + selected);
            staffGlobal = staffFull.stream().filter(col -> col.toString().trim().equalsIgnoreCase(selected.trim())).findFirst().orElse(null);
            System.out.println("Selectd Global dept: " + staffGlobal);
        } else {
            System.out.println("Selected Item is null");
        }
    }

    /**
     * Sets the deptHead combo with staffs in that college on change in college
     * selection
     *
     * @param cmbDeptHead
     * @param college
     */
    private void setDeptHeadCombo(JComboBox<String> cmbDeptHead, College college) {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cmbDeptHead.getModel();
        model.removeAllElements();

        staffFull.stream().
                filter(staff -> deptFull.stream().anyMatch(dept -> dept.getCollegeId() == college.getCollegeId() && staff.getDeptId() == dept.getDeptId()))
                .filter(staff -> staff.getRoleId() == (rolesFull.stream().filter(role -> role.getRoleName().equalsIgnoreCase("HOD")).findFirst().get().getRoleId()))
                .forEach(staff -> model.addElement(staff.toString()));

        cmbDeptHead.setModel(model);

        if (model.getSize() > 0) {
            cmbDeptHead.setSelectedIndex(0);
        }
    }

