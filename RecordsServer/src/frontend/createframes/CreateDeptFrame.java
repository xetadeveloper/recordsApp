    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        if (confirmAction("Proceed With Department Creation?")) {
            System.out.println("Dept creation confirmed");
            if (validateAllFields()) {

                String deptQuery = String.format("Insert into Departments (DeptName, DeptHead_StaffID, Dept_CollegeID, Dept_StaffNumber, Dept_ProgrammeNumber) values\n"
                        + "('%s', %d, %d, 0, 0);",
                        txtDeptName.getText().trim(),
                        (staffGlobal != null ? staffGlobal.getStaffId() : 0),
                        collegeGlobal.getCollegeId());

                boolean deptInserted = server.sendUpdateQuery(deptQuery);

                if (deptInserted) {
                    String progQuery = String.format("Update Colleges set College_DeptNumber = %d where CollegeID = %d;",
                            (collegeGlobal.getCollegeDeptNumber() + 1),
                            collegeGlobal.getCollegeId());

                    if (server.sendUpdateQuery(progQuery)) {
                        displayMessage("Department  has been created", "Department Creation Successful");
                    } else {
                        deleteLastInsert("Departments", "DeptID", server);
                        displayErrorMessage("Error in updating college department count. Contact admin", "Update Failed");
                    }

                    dispose();
                } else {
                    displayErrorMessage("Error in creating department. Contact admin", "Update Failed");
                    dispose();
                }

            } else {
                displayErrorMessage("A wrong value has been entered into one of the fields. Please check through", "Update Failed");
            }
        }
    }//GEN-LAST:event_btnCreateActionPerformed

