    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (confirmAction("Proceed With Department Creation?")) {
            System.out.println("Dept creation confirmed");
            if (validateAllFields()) {

                String deptQuery = String.format("Update Departments set DeptName = '%s', DeptHead_StaffID = %d, Dept_CollegeID = %d where DeptId = %d;",
                        txtDeptName.getText().trim(),
                        (staffGlobal != null ? staffGlobal.getStaffId() : 0),
                        collegeGlobal.getCollegeId(),
                        dept.getDeptId());

                boolean deptUpdated = server.sendUpdateQuery(deptQuery);

                if (deptUpdated) {
                    displayMessage("Department  has been updated", "Department Update Successful");
                    dispose();
                } else {
                    displayErrorMessage("Error in updating department. Contact admin", "Update Failed");
                    dispose();
                }
            } else {
                displayErrorMessage("A wrong value has been entered into one of the fields. Please check through", "Update Failed");
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

