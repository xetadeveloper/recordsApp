    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        //Validate entry first
        String user = txtUser.getText();
        char[] pass = pswdPass.getPassword();

        if (new ServerLogin(user, pass).done) {
            JOptionPane.showMessageDialog(this, "Logged In", "Message", JOptionPane.INFORMATION_MESSAGE); //After validation if correct display logged in JOption dialog
            this.setVisible(false);
            System.out.println("About to open serverpanelframe");
            MainServer server = new MainServer();
            System.out.println("Opening server panel");

            try {
                ServerPanelFrame servPanel = new ServerPanelFrame(server);
                System.out.println("Finisjed woth constructor");
                servPanel.setVisible(true); //Open mainframe
            } catch (ExceptionInInitializerError err) {
                err.getCause().printStackTrace();
            }

        } else {
            lblError.setText("Invalid login credentials or user does not have privilege to access this server");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

