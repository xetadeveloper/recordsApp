    private void initDeptFullDataPanel(int deptID) {
        deptList.stream().forEach(s -> System.out.println(s.getDeptId()));
        Department dept = deptList.stream().filter(dep -> dep.getDeptId() == deptID).findFirst().get();

        lblDeptName.setText(dept.getDeptName());
        lblProgNum.setText(String.valueOf(dept.getProgNumber()));
        lblDeptHead.setText(getDeptHeadName(dept.getDeptHeadStaffID(), "FullName"));
        lblStaffNum.setText(String.valueOf(dept.getStaffNumber()));
    }

    /**
     * Gets the head of the dept's name
     *
     * @param staffID
     * @return
     */
    private String getDeptHeadName(int staffID, String alias) {
        String query = String.format("Select concat(FirstName, ' ' ,LastName) as %s from staffs where staffID = %d;",
                alias, staffID);
        String name = String.valueOf(server.getQuerySingleResult(query, alias));
        System.out.println("Name: " + name);
        return name.equalsIgnoreCase("0") ? "N/A" : name;
    }

    /**
     * Adds a document listener to the text field for search purposes
     *
     * @param field
     */
    private void initDocumentListener(JTextField field) {
        field.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                String text = field.getText().trim();
                searchText += text.charAt(text.length() - 1);
                searchDept(tblDepts, deptList, searchType, searchText, columnNames);
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                if (searchText.length() > 0) {
                    searchText = searchText.substring(0, searchText.length() - 1);
                    searchDept(tblDepts, deptList, searchType, searchText, columnNames);
                } else {
                    initTableRecordsDept(tblDepts, deptList, columnNames);
                }
            }

            @Override
            public void changedUpdate(DocumentEvent e) {

            }
        });
    }


