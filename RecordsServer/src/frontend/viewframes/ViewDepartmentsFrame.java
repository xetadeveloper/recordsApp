    private void tblDeptsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDeptsMouseClicked

        if (evt.getClickCount() == 2) {
            Point location = evt.getLocationOnScreen();

            int row = tblDepts.getSelectedRow();
            if (row > -1) {
                DefaultTableModel model = (DefaultTableModel) tblDepts.getModel();

                deptID = Integer.parseInt(String.valueOf(model.getValueAt(row, 0)));

                dept = deptList.stream().
                        filter(dept -> dept.getDeptId() == deptID).
                        findFirst().
                        orElse(null);

                popStudentOptions.setLocation(location.x, location.y);
                popStudentOptions.setVisible(true);
            }
        } else {
            popStudentOptions.setVisible(false);
        }
    }//GEN-LAST:event_tblDeptsMouseClicked

