    private void tblProgMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProgMouseClicked

        if (evt.getClickCount() == 2) {
            Point location = evt.getLocationOnScreen();

            int row = tblProg.getSelectedRow();
            if (row > -1) {
                DefaultTableModel model = (DefaultTableModel) tblProg.getModel();

                progID = Integer.parseInt(String.valueOf(model.getValueAt(row, 0)));
                programme = progList.stream().filter(pr -> pr.getProgrammeId() == progID).findFirst().get();

                popStudentOptions.setLocation(location.x, location.y);
                popStudentOptions.setVisible(true);
            }
        } else {
            popStudentOptions.setVisible(false);
        }
    }//GEN-LAST:event_tblProgMouseClicked

