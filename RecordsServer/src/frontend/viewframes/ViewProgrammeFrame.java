    private void initProgFullDataPanel(int progID) {
        Programme prog = progList.stream().filter(pr -> pr.getProgrammeId() == progID).findFirst().get();
        Department dept = deptList.stream().filter(depts -> depts.getDeptId() == prog.getDeptId()).findFirst().get();

        lblProgName.setText(prog.getProgrammeName());
        lblProgFee.setText(String.valueOf(getFormattedCurrencyValue(prog.getProgrammeFee())));
        lblCourseNum.setText(String.valueOf(prog.getProgCourseNumber()));
        lblMatricType.setText(String.valueOf(prog.getMatricType()));
        lblProgDept.setText(dept.getDeptName());
        lblMaxLevel.setText(String.valueOf(prog.getMaximumLevel()));
        lblStuNum.setText(String.valueOf(prog.getStudentNumber()));
    }
    
         
    /**
     * Adds a document listener to the text field for search purposes
     *
     * @param field
     */
    private void initDocumentListener(JTextField field) {
        field.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                String text = field.getText().trim();
                searchText += text.charAt(text.length() - 1);
                searchProgr(tblProg, progList, searchType, searchText, columnNames);
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                if (searchText.length() > 0) {
                    searchText = searchText.substring(0, searchText.length() - 1);
                    searchProgr(tblProg, progList, searchType, searchText, columnNames);
                }
                else{
                    initTableRecordsProg(tblProg, progList, columnNames);
                }
            }

            @Override
            public void changedUpdate(DocumentEvent e) {

            }
        });
    }

