    private void tblStaffsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStaffsMouseClicked

        if (evt.getClickCount() == 2) {
            Point location = evt.getLocationOnScreen();

            int row = tblStaffs.getSelectedRow();
            DefaultTableModel model = (DefaultTableModel) tblStaffs.getModel();

            staffID = Integer.parseInt(String.valueOf(model.getValueAt(row, 0)));

            staff = staffList.stream().
                    filter(staff -> staff.getStaffId() == staffID).
                    findFirst().
                    orElse(null);

            staffID = staff.getStaffId();

            roleGlobal = rolesFull.stream().
                    filter(role -> role.getRoleId() == staff.getRoleId()).
                    findFirst().
                    orElse(null);

            popStudentOptions.setLocation(location.x, location.y);
            popStudentOptions.setVisible(true);
        } else {
            popStudentOptions.setVisible(false);
        }
    }//GEN-LAST:event_tblStaffsMouseClicked

