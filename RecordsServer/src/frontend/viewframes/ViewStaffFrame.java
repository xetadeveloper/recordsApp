    private void initStaffFullDataPanel(int staffID) {
        College col = collegeList.stream().filter(coll -> coll.getCollegeStaffID() == staffID).findFirst().get();
        Department dept = deptList.stream().filter(deptl -> deptl.getDeptStaffID() == staffID).findFirst().get();
        Staff staff = staffList.stream().filter(stf -> stf.getStaffId() == staffID).findFirst().get();

        lblCollege.setText(col.getCollegeName());
        lblDept.setText(dept.getDeptName());
        lblFirstName.setText(staff.getFirstName());
        lblLastName.setText(staff.getLastName());
        setLabelImage(staff.getPassport(), lblPassport);
        lblRole.setText(roleGlobal.getRoleName());
    }

    /**
     * Adds a document listener to the text field for search purposes
     *
     * @param field
     */
    private void initDocumentListener(JTextField field) {
        field.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                String text = field.getText().trim();
                searchText += text.charAt(text.length() - 1);
                searchStaff(tblStaffs, staffList, rolesFull, searchType, searchText, columnNames);
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                if (searchText.length() > 0) {
                    searchText = searchText.substring(0, searchText.length() - 1);
                    searchStaff(tblStaffs, staffList, rolesFull, searchType, searchText, columnNames);
                } else {
                    initTableRecordsStaff(tblStaffs, staffList, rolesFull, columnNames);
                }
            }

            @Override
            public void changedUpdate(DocumentEvent e) {

            }
        });
    }


