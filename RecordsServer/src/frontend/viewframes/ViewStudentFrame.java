    private void itemRestartRegistrationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemRestartRegistrationActionPerformed
        if (confirmAction("Do you want this student to restart the course registration process if started/finished already")) {
            String query = String.format("Update students set RegistrationAllowed = false, CourseAdvisorApproved = false, HODApproved = false, DeanApproved = false, StudentRegistered = false, \n"
                    + "Student_MaxUnits = (select MaximumUnits from sessiondetails where sessionID = (Select Max(SessionId) from sessiondetails)) \n"
                    + "where MatricNo = %d;",
                    matricNo);
            if (server.sendUpdateQuery(query)) {
                System.out.println("Matric No: " + matricNo);
                String clearRegQuery = String.format("Delete from RegisteredCourses where Reg_MatricNo = %s ;", matricNo);

                System.out.println("Query:  " + clearRegQuery);
                if (server.sendUpdateQuery(clearRegQuery)) {
                    displayMessage("Student registration restarted", "Restart Successful");
                } else {
                    displayErrorMessage("Student registration restart failed. Contact Maintenace Team", "Restart Failed");
                    String resetQuery = String.format("Update students set RegistrationAllowed = %b, CourseAdvisorApproved = %b, HODApproved = %b, DeanApproved = %b, StudentRegistered = %b,\n"
                            + " Student_MaxUnits = %d\n"
                            + " where MatricNo = %d;",
                            student.isRegistrationAllowed(),
                            student.isCourseAdvisorApproved(),
                            student.isHODApproved(),
                            student.isDeanApproved(),
                            student.isStuRegistered(),
                            student.getStudentMaxUnits(),
                            matricNo);

                    if (server.sendUpdateQuery(resetQuery)) {
                        logger.logErrorNormal("Student Registration Couldn't Be Restarted. Contact Maintenance Team",
                                new Exception("Couldn't restart registration"));
                    } else {
                        logger.logErrorNormal("Couldn't revert student back to his old data on restarting registration failed",
                                new Exception("Couldn't revert student back to his old data upon restarting registration"));
                    }
                }
            } else {
                displayErrorMessage("Student registration restart failed", "Restart Failed");

            }
        }
    }//GEN-LAST:event_itemRestartRegistrationActionPerformed

